def jia_fa( num1,num2):
  res = num1 + num2
  return res

def jian_fa( num1,num2):
  res = num1 - num2
  return res

def chen_fa( num1,num2):
  res = num1 * num2
  return res

def chu_fa( num1,num2):
  res = num1 / num2
  return res



jiafa = jia_fa(30,2)
print(jiafa)

